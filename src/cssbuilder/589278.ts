export class CssBuilder extends CssMixins {
  rules: [string, string][]

  constructor(rules?: [string, string][]) {
    super()
    this.rules = rules || []
  }

  /**
   * Adds rules from another CssBuilder.
   */
  add(other: CssBuilder): CssBuilder {
    return new CssBuilder([...this.rules, ...other.rules])
  }

  /**
   * Adds a single rule.
   */
  acs(property: string, value: string): CssBuilder {
    return new CssBuilder([...this.rules, [property, value]])
  }

  /**
   * Generates the CSS class string.
   */
  get $(): string {
    const map = new Map<string, string>()
    for (const [prop, val] of this.rules.reverse()) {
      if (!map.has(prop)) {
        map.set(prop, val)
      }
    }
    return [...map.values()].reverse().join(' ')
  }

  /**
   * Conditionally adds rules.
   */
  if(condition: boolean, trueRules: CssBuilder, falseRules?: CssBuilder): CssBuilder {
    return condition ? this.add(trueRules) : falseRules ? this.add(falseRules) : this
  }

  /**
   * Case-based rule addition.
   */
  case(cases: [boolean, CssBuilder][], defaultRules?: CssBuilder): CssBuilder {
    for (const [condition, rules] of cases) {
      if (condition) {
        return this.add(rules)
      }
    }
    return defaultRules ? this.add(defaultRules) : this
  }

  /**
   * Matches a key to rules.
   */
  match(key: string, ruleMap: Record<string, CssBuilder>): CssBuilder {
    const rules = ruleMap[key]
    return rules ? this.add(rules) : this
  }

  /**
   * Applies rules based on object properties.
   */
  with(options: Record<string, boolean>): CssBuilder {
    const keys = Object.keys(options)
    if (keys.length === 0) {
      return this
    }
    return keys.reduce((builder, key) => options[key] ? builder[key] : builder, this)
  }
}

