type DeepKey<T, Depth extends number, CurrentDepth extends number = 0> = 
  CurrentDepth extends Depth
    ? keyof T
    : T extends Record<string, any>
      ? {
          [K in keyof T]: DeepKey<T[K], Depth, Inc<CurrentDepth>>;
        }[keyof T]
      : never;

